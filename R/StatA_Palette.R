    "red2" = red2,
    "red3" = red3,
    "red4" = red4,
    "red5" = red5,
    "red6" = red6,
    "violet1" = violet1,
    "violet2" = violet2,
    "violet3" = violet3,
    "violet4" = violet4,
    "violet5" = violet5,
    "violet6" = violet6,
    "yellow1" = yellow1,
    "yellow2" = yellow2,
    "yellow3" = yellow3,
    "yellow4" = yellow4,
    "yellow5" = yellow5,
    "yellow6" = yellow6,
    "azure1" = azure1,
    "azure2" = azure2,
    "azure3" = azure3,
    "azure4" = azure4,
    "azure5" = azure5,
    "azure6" = azure6,
    "green1" = green1,
    "green2" = green2,
    "green3" = green3,
    "green4" = green4,
    "green5" = green5,
    "green6" = green6,
    "rainbow1" = rainbow1,
    "rainbow2" = rainbow2,
    "rainbow3" = rainbow3,
    "rainbow4" = rainbow4,
    "rainbow5" = rainbow5,
    "pastel1" = pastel1,
    "pastel2" = pastel2,
    "pastel3" = pastel3,
    "pastel4" = pastel4,
    "pastel5" = pastel5,
    "pastel6" = pastel6,
    "pastel7" = pastel7,
    "pastel8" = pastel8,
    "grey1" = grey1,
    "grey2" = grey2,
    "grey3" = grey3,
    "grey4" = grey4,
    "grey5" = grey5,
