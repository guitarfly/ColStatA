# Display Palette Function ####
#' Display and return a color palette
#'
#' This function returns the specified color palette and also displays it using a barplot.
#'
#' @param palette_name The name of the color palette to display (e.g., "yellow6").
#' @return A vector of color hex codes.
#' @export
#' @examples
#' display_palette("yellow6")
display_palette <- function(palette_name) {
  palettes <- list(
    "brown1" = brown1,
    "brown2" = brown2,
    "brown3" = brown3,
    "brown4" = brown4,
    "brown5" = brown5,
    "brown6" = brown6,
    "blue1" = blue1,
    "blue2" = blue2,
    "blue3" = blue3,
    "blue4" = blue4,
    "blue5" = blue5,
    "blue6" = blue6,
